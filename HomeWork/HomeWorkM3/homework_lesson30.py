
class Study:

    def __init__(self, type_, years, study_lvl):

        self.type_ = type_
        self.years = years
        self.study_lvl = study_lvl
    
    def match(self):
        return f'Тип: {self.type_}; Длительность учебы: {self.years}; Уровень учебы: {self.study_lvl} '






































